    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nome = jTextField1.getText();
        String funcao = jTextField2.getText();
        String registro = jTextField3.getText();
        try{
            if(nome.equals("")||funcao.equals("")||registro.equals("")){
                throw new Exception("Preencha todos os campos!");
            }
            Departamento departamento = (Departamento) jComboBox1.getSelectedItem();
            Funcionario funcionario = new Funcionario(nome, funcao, registro, departamento);
            FuncionarioJpaController fjc = new FuncionarioJpaController(emf);
            fjc.create(funcionario);            
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
        } catch(Exception e){
            JOptionPane.showMessageDialog(this,e.getMessage(),"Erro",JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

